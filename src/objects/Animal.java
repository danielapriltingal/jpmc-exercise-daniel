public class Animal {


    private String animalType;
    private int eggsLay;
    private boolean animalDoLayEggs;

    public String getAnimalType() {
        return animalType;
    }

    public void setAnimalType(String animalType) {
        this.animalType = animalType;
    }

    public int getEggsLay() {
        return eggsLay;
    }

    public void setEggsLay(int eggsLay) {
        this.eggsLay = eggsLay;
    }

    public boolean isAnimalDoLayEggs() {
        return animalDoLayEggs;
    }

    public void setAnimalDoLayEggs(boolean animalDoLayEggs) {
        this.animalDoLayEggs = animalDoLayEggs;
    }

}
